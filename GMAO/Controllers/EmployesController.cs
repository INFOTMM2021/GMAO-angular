using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using GMAO.Data;
using GMAO.Models;
using Microsoft.Extensions.Configuration;
using System.Data;
using Microsoft.Data.SqlClient;

namespace GMAO.Controllers
{
    [Route("api/[controller]")]
    public class EmployesController : Controller
    {
        private readonly GMAOContext _context;
        private readonly IConfiguration _configuration;

        public EmployesController(GMAOContext context, IConfiguration configuration)
        {
            _context = context;
            _configuration = configuration;
        }

        // GET: Employes

        [Route("GetAllEmployesActif")]
        [HttpGet]
        // GET: Pieces
        public JsonResult Getem()
        {
            string query = @"select * from Employe where Actif='Oui'";

            DataTable table = new DataTable();
            string sqlDataSource = _configuration.GetConnectionString("GMAOContext");
            SqlDataReader MyReader;

            using (SqlConnection myConn = new SqlConnection(sqlDataSource))
            {
                myConn.Open();
                using (SqlCommand myCommand = new SqlCommand(query, myConn))
                {
                    MyReader = myCommand.ExecuteReader();
                    table.Load(MyReader);
                    MyReader.Close();
                    myConn.Close();
                }
            }

            return new JsonResult(table);
        }


        [Route("GetAllEmployesToList")]
        [HttpGet]
        public List<Employe> Get() => _context.Employe.OrderBy(p => p.Nom).ToList();








        [Route("UpdateActif")]
        [HttpPut]
        
        public JsonResult UpdateActif(string Matricule)
        {
            string query = @"update Employe set Actif='Non' where Matricule= @Matricule";

            DataTable table = new DataTable();
            string sqlDataSource = _configuration.GetConnectionString("GMAOContext");
            SqlDataReader MyReader;

